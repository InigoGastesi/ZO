#include "aes.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main(){

    uint8_t in[]={0xdf, 0x14, 0xe0, 0xd9, 0xf0, 0xf8, 0x78, 0x9b, 0x38, 0x73, 0x12, 0xb3, 0x9f, 0xb5, 0x79, 0x27};
    uint8_t key[]={0x70, 0x72, 0x69, 0x76, 0x61, 0x74, 0x65, 0x6b, 0x65, 0x79, 0x61, 0x65, 0x73, 0x63, 0x79, 0x70, 0x68, 0x65, 0x72, 0x6b, 0x75, 0x74, 0x78, 0x61, 0x62, 0x61, 0x6e, 0x6b, 0x00, 0x00, 0x00, 0x00};
    uint8_t iv[]={0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,  0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};
    uint8_t out[16];
    struct AES_ctx ctx;

    AES_init_ctx_iv(&ctx, key, iv);
    AES_CBC_decrypt_buffer(&ctx, in, 16);

    printf("CBC encrypt: ");
    if (0 == memcmp((char*) out, (char*) in, 16)) {
        printf("SUCCESS!\n");
	return(0);
    } else {
        printf("FAILURE!\n");
	return(1);
    }
    return 0;
}